# 表記揺れの正規化マップ
PREF_NORMALIZE = {
    "東京":   "東京都",
    "大阪":   "大阪府",
    "京都":   "京都府",
    "北海道": "北海道",
}
# その他は「〜県」を付与
for _name in PREF_CODES.values():
    PREF_NORMALIZE[_name] = _name


def normalize_pref_name(name: str) -> str:
    """都道府県名を正規化（「東京」→「東京都」等）"""
    name = name.strip()
    if name in PREF_NORMALIZE:
        return PREF_NORMALIZE[name]
    # 末尾に「都道府県」がなければ「県」を付与
    if not name.endswith(("都", "道", "府", "県")):
        return name + "県"
    return name
